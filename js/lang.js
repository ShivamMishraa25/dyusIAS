// Function to change language
function setLanguage(lang) {
    localStorage.setItem("language", lang);
    document.querySelector(".language-switch").innerText = lang === "en" ? "हिन्दी" : "English";
    
    document.getElementById("nav-home").innerText = translations[lang].home;
    document.getElementById("nav-about").innerText = translations[lang].about;
    document.getElementById("nav-courses").innerText = translations[lang].courses;
    document.getElementById("nav-faculty").innerText = translations[lang].faculty;
    document.getElementById("nav-announcements").innerText = translations[lang].announcements;
    document.getElementById("nav-contact").innerText = translations[lang].contact;
    document.getElementById("register-btn").innerText = translations[lang].register;
    document.getElementById("explore-btn").innerText = translations[lang].explore_courses;
    document.getElementById("hero-title").innerHTML = translations[lang].hero_title;
    document.getElementById("hero-text").innerText = translations[lang].hero_text;
    document.getElementById("why-choose-title").innerText = translations[lang].chooseTitle;
    document.getElementById("why-choose-des").innerText = translations[lang].chooseDes;
    document.getElementById("why-choose-h1").innerText = translations[lang].choose_h1;
    document.getElementById("why-choose-h2").innerText = translations[lang].choose_h2;
    document.getElementById("why-choose-h3").innerText = translations[lang].choose_h3;
    document.getElementById("why-choose-p1").innerText = translations[lang].choose_p1;
    document.getElementById("why-choose-p2").innerText = translations[lang].choose_p2;
    document.getElementById("why-choose-p3").innerText = translations[lang].choose_p3;
    document.getElementById("about-title").innerText = translations[lang].about_title;
    document.getElementById("about-subtitle").innerText = translations[lang].about_subtitle;
    document.getElementById("about-title2").innerText = translations[lang].about_title2;
    document.getElementById("about-text1").innerText = translations[lang].about_text1;
    document.getElementById("about-text2").innerText = translations[lang].about_text2;
    document.getElementById("courses-title").innerText = translations[lang].courses_title;
    document.getElementById("courses-description").innerText = translations[lang].courses_description;
    document.getElementById("faculty-title").innerText = translations[lang].faculty_title;
    document.getElementById("faculty-description").innerText = translations[lang].faculty_description;
    document.getElementById("announcements-title").innerText = translations[lang].announcements_title;
    document.getElementById("announcements-description").innerText = translations[lang].announcements_description;
    document.getElementById("contact-title").innerText = translations[lang].contact_title;
    document.getElementById("contact-description").innerText = translations[lang].contact_description;

    // Register section translations
    document.getElementById("register-title").innerText = translations[lang].registerTitle;
    document.getElementById("register-description").innerText = translations[lang].registerDescription;
    document.getElementById("register-name").placeholder = translations[lang].registerName;
    document.getElementById("register-email").placeholder = translations[lang].registerEmail;
    document.getElementById("register-phone").placeholder = translations[lang].registerPhone;
    document.getElementById("register-message").placeholder = translations[lang].registerMessage;
    document.getElementById("register-submit").innerText = translations[lang].registerSubmit;

    const aboutFeatures = document.querySelectorAll(".about-features li");
    translations[lang].about_features.forEach((feature, index) => {
        aboutFeatures[index].innerText = feature;
    });

    document.querySelector(".about-btn").innerText = translations[lang].about_button;
}

// Detect stored language or default to Hindi
const savedLang = localStorage.getItem("language") || "hi";
setLanguage(savedLang);

// Event Listener for language switch
document.querySelector(".language-switch").addEventListener("click", function (event) {
    event.preventDefault();
    const newLang = localStorage.getItem("language") === "en" ? "hi" : "en";
    setLanguage(newLang);
});
